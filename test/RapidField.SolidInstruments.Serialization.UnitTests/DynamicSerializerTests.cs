using FluentAssertions;
using Microsoft.VisualStudio.TestTools.UnitTesting;

namespace RapidField.SolidInstruments.Serialization.UnitTests
{
    [TestClass]
    public class DyanamicSerializerTests
    {
        [TestMethod]
        public void ShouldBeSerializable_UsingBinaryFormat()
        {
            // Arrange.
            var format = SerializationFormat.Binary;

            // Assert.
            ShouldBeSerializable(format);
        }

        [TestMethod]
        public void ShouldBeSerializable_UsingCompressedJsonFormat()
        {
            // Arrange.
            var format = SerializationFormat.CompressedJson;

            // Assert.
            ShouldBeSerializable(format);
        }

        [TestMethod]
        public void ShouldBeSerializable_UsingCompressedXmlFormat()
        {
            // Arrange.
            var format = SerializationFormat.CompressedXml;

            // Assert.
            ShouldBeSerializable(format);
        }

        [TestMethod]
        public void ShouldBeSerializable_UsingJsonFormat()
        {
            // Arrange.
            var format = SerializationFormat.Json;

            // Assert.
            ShouldBeSerializable(format);
        }

        [TestMethod]
        public void ShouldBeSerializable_UsingXmlFormat()
        {
            // Arrange.
            var format = SerializationFormat.Xml;

            // Assert.
            ShouldBeSerializable(format);
        }

        private static void ShouldBeSerializable(SerializationFormat format)
        {
            // Arrange.
            var targetType = typeof(SimulatedObject);
            var serializationTarget = SimulatedObject.Random();

            // Act.
            var serializeResult = DynamicSerializer.Serialize(serializationTarget, targetType, format);
            var deserializeResult = DynamicSerializer.Deserialize(serializeResult, targetType, format) as SimulatedObject;

            // Assert.
            deserializeResult.Should().Be(serializationTarget);
        }
    }
}